// Variables

#include <stdio.h>

int main () {
	// Declaraciones
	int h;
	float o;
	double r;
	char c;
	
	// Nombramientos
	h = 8;
	o = 8.8;
	r = 8.88;
	c = 'H';
	
	// Imprimir
	printf("%i",h);
	printf("\n\n");
	printf("%f",o);
	printf("\n\n");
	printf("%f",r);
	printf("\n\n");
	printf("%c",c);
	printf("\n\n");
	
	system("pause");
	return 0;
}
